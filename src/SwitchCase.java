
public class SwitchCase {

	public static void main(String[] args) {
		int i =3;
		switch(i) {
		case 1:
			System.out.println("I am One");
			break;
		case 2:
			System.out.println("I am Two");
			break;
		case 3:
			System.out.println("I am Three");
			break;
		default:
			System.out.println("Invalid Values");
		}

	}

}
